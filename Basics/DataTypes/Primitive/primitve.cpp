#include <bits/stdc++.h>
using namespace std;

int main()
{
	cout << "Size of int : " << sizeof(int) << endl;                    // 4
	cout << "Size of char : " << sizeof(char) << endl;                  // 1
	cout << "Size of bool : " << sizeof(bool) << endl;                  // 1
	cout << "Size of float : " << sizeof(float) << endl;                // 4  
	cout << "Size of double : " << sizeof(double) << endl;              // 8
	// cout << "Size of void : " << sizeof(void) << endl;               // Value less quantity
	cout << "Size of wide character : " << sizeof(wchar_t) << endl;     // 2
	
    
	return 0;
}
